# list of Python is similar to Array
# index   0   1   2   3   4
number = [45, 51, 49 ,82 ,5]
# index   -5  -4  -3  -2  -1

# list(start : end)
print(number[2:4])
print(number[:])   # copy list
print(number[0:])
print(number[:3])

# list(start : end : step)
print(number[0:4:1])
print(number[0:4:2])
print(number[4:0:-1])
print(number[4:0:-2])
print(number[::-1])  #reversed list

